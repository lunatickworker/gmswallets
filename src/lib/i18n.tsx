export function FlagUS({ size = 20 }: { size?: number }) {
  const h = size * 0.526;
  return (
    <svg width={size} height={h} viewBox="0 0 38 20" xmlns="http://www.w3.org/2000/svg">
      <rect width="38" height="20" fill="#B22234" />
      {[1,3,5,7,9,11].map((y) => <rect key={y} x="0" y={y * 20/13} width="38" height={20/13} fill="#fff" />)}
      <rect width="15" height={20 * 7/13} fill="#3C3B6E" />
      {/* Stars simplified as dots */}
      {Array.from({ length: 50 }, (_, i) => {
        const row = Math.floor(i / 6);
        const col = i % 6;
        if (row > 8) return null;
        const offset = row % 2 === 0 ? 0 : 1.25;
        return <circle key={i} cx={1.5 + col * 2.5 + offset} cy={1.1 + row * 1.05} r="0.35" fill="#fff" />;
      })}
    </svg>
  );
}

