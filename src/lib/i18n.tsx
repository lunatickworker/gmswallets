type TranslationKey = keyof typeof translations.ko;

// ─── Context ──────────────────────────────────────────────────────────────────

interface I18nContextValue {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: TranslationKey | string) => string;
}

const I18nContext = createContext<I18nContextValue>({
  lang: "ko",
  setLang: () => {},
  t: (key) => (translations.ko as any)[key] ?? key,
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const stored = (typeof localStorage !== "undefined" && localStorage.getItem("lang")) as Lang | null;
  const [lang, setLangState] = useState<Lang>(stored ?? "ko");

  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem("lang", l);
  };

  const t = (key: TranslationKey | string): string => (translations[lang] as any)[key] ?? (translations.ko as any)[key] ?? key;

  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}

// ─── Language Switcher Component ──────────────────────────────────────────────

export function LanguageSwitcher({ className = "" }: { className?: string }) {
  const { lang, setLang } = useI18n();

  return (
    <div className={`flex items-center gap-1 bg-secondary/60 border border-border rounded-sm p-0.5 ${className}`}>
      <button
        onClick={() => setLang("ko")}
        className={`flex items-center gap-1.5 px-2 py-1 rounded-sm font-mono text-[12px] uppercase tracking-widest transition-colors ${
          lang === "ko" ? "bg-background text-foreground shadow-sm" : "text-muted-foreground hover:text-foreground"
        }`}
        title="한국어"
      >
        <FlagKR size={18} />
        <span className="hidden sm:inline">KO</span>
      </button>
      <button
        onClick={() => setLang("en")}
        className={`flex items-center gap-1.5 px-2 py-1 rounded-sm font-mono text-[12px] uppercase tracking-widest transition-colors ${
          lang === "en" ? "bg-background text-foreground shadow-sm" : "text-muted-foreground hover:text-foreground"
        }`}
        title="English"
      >
        <FlagUS size={18} />
        <span className="hidden sm:inline">EN</span>
      </button>
    </div>
  );
}
